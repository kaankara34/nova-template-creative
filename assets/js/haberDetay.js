  // URL'den id parametresini al
  const urlParams = new URLSearchParams(window.location.search);
  const newsId = parseInt(urlParams.get("id"));
  const paragraphsContainer = document.getElementById("paragraphs");
  // Haber verisini id'ye göre bul
  const newsItem = newsData.find((item) => item.id === newsId);

  if (newsItem) {
    document.title = `NOVA © - ${newsItem.title}`;
    document.getElementById("post_photo").src = newsItem.postPhoto;
    document.getElementById("news-title").querySelector("span").textContent =
      newsItem.title;
    document.getElementById("news-title-2").textContent = newsItem.title;
    document.getElementById("news-date").textContent = newsItem.date;
    // Önceki ve sonraki haber id'lerini al
    const newsIds = newsData.map((news) => news.id);
    console.log(newsIds);
    const currentNewsIndex = newsIds.indexOf(newsId);
    console.log(currentNewsIndex);

    // Eğer mevcut haber ilk haberse, önceki haber sonuncu haber olacak
    const prevNewsIndex =
      currentNewsIndex === 0 ? newsIds.length - 1 : currentNewsIndex - 1;

    // Eğer mevcut haber sonuncu haberse, sonraki haber ilk haber olacak
    const nextNewsIndex =
      currentNewsIndex === newsIds.length - 1 ? 0 : currentNewsIndex + 1;

    // Önceki haber
    const prevNewsId = newsIds[prevNewsIndex];
    const prevNewsUrl = `haber-detay.html?id=${prevNewsId}`;
    document
      .querySelector(".page-navigation__prev")
      .setAttribute("href", prevNewsUrl);

    // Sonraki haber
    const nextNewsId = newsIds[nextNewsIndex];
    const nextNewsUrl = `haber-detay.html?id=${nextNewsId}`;
    document
      .querySelector(".page-navigation__next")
      .setAttribute("href", nextNewsUrl);

    // Dinamik URL
    const currentUrl = window.location.href;

    const whatsappLink = `https://wa.me/?text=${encodeURIComponent(
      "NOVA'nın " + "\""+ newsItem.title +"\""+ " haberine göz atın. " + currentUrl
    )}`;
    // Get the container where you want to append the new element
    const socialContainer = document.getElementById("social-link");

    // Set innerHTML for the new WhatsApp link
    socialContainer.innerHTML += `
  <li>
    <a href="${whatsappLink}" class="onovo-social-link onovo-hover-2 whatsapp-link">
      <i class="icon fab fa-whatsapp"></i>
    </a>
  </li>
`;

    // Paragrafları dinamik olarak ekle

    const paragraphs = paragraphsContainer.querySelectorAll("p");

    paragraphs.forEach((p) => p.remove());

    newsItem.paragraphs.forEach((paragraph) => {
      const p = document.createElement("p");
      p.textContent = paragraph;
      paragraphsContainer.appendChild(p);
    });
  }
  const existingBlockquote = document.getElementById("news-blockquote");
  const existingAuthor = document.getElementById("blockquote-author");
  if (existingBlockquote) {
    existingBlockquote.remove();
  }
  if (existingAuthor) {
    existingAuthor.remove();
  }

  if (newsItem.blockquote) {
    // Önce mevcut blockquote öğesini sil

    // Yeni blockquote öğesi oluştur
    const blockquote = document.createElement("blockquote");
    blockquote.id = "news-blockquote";

    // İçine text paragrafını ekle
    const textParagraph = document.createElement("p");
    textParagraph.id = "blockquote-text";
    textParagraph.textContent = newsItem.blockquote.text;
    blockquote.appendChild(textParagraph);

    // Yazar paragrafını ekle, yalnızca yazar mevcutsa
    if (newsItem.blockquote.author) {
      const authorParagraph = document.createElement("p");
      const cite = document.createElement("cite");
      cite.id = "blockquote-author";
      cite.textContent = newsItem.blockquote.author;
      authorParagraph.appendChild(cite);
      blockquote.appendChild(authorParagraph);
    }

    paragraphsContainer.appendChild(blockquote);
    if (newsItem.paragraphs2) {
      newsItem.paragraphs2.forEach((paragraph) => {
        const p = document.createElement("p");
        p.textContent = paragraph;
        paragraphsContainer.appendChild(p);
      });
    }
  }

